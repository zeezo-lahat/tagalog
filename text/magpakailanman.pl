

MAGPAKAILANMAN

   root words: kailan + man

   magpakailanman
   forevermore

   Mamahalin kita magpakailanman.
   I will love you forevermore.

   Babantayan kita magpakailanman.
   I will forever watch over you.

   Mabuhay magpakailanman sa paraiso.
   Live forever in paradise.

   Mabuhay tayo magpakailanman sa paraiso.
   Let us live forever in paradise.
